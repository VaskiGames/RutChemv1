package com.rutchem.app

// Model danych - teraz ma dwie nazwy
data class Element(
    val number: Int,
    val symbol: String,
    val namePl: String, // Nazwa PL
    val nameEn: String, // Nazwa EN
    val mass: Double,
    val group: Int
)

// Pomocnicza funkcja: zwraca nazwę w wybranym języku
fun Element.getName(lang: AppLanguage): String {
    return if (lang == AppLanguage.PL) this.namePl else this.nameEn
}

